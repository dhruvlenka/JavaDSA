package CoreJava.Operators;

public class ArithmeticOperator {
    public static void main(String[] args) {
        int a = 2 + 4;
        int b = a - 2;
        int c = b * 4;
        int d = c / 4;
        System.out.println("Addition of a = " + a);
        System.out.println("Substraction of b = " + b);
        System.out.println("Multiplication of c = " + c);
        System.out.println("Modulos of d = " + d);

        int x = 9;
        int y = 2;
        int add = x+y;
        int subs = x-y;
        int multi = x*y;
        int divi = x/y;
        int modulo = x%y;
        System.out.println("Addition of 9 + 2 = " + add);
        System.out.println("Substraction of 9 - 2 = " + subs);
        System.out.println("Multiplication of 9 * 2 = " + multi);
        System.out.println("Division of 9 / 2 = " + divi);
        System.out.println("Modulus of 9 % 2 = " + modulo);
    }
}


