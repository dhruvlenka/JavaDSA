package CoreJava.Operators;

public class AssignmentOperator {
    public static void main(String[] args) {
        int x = 21;
        x += 3; //21+3 = 24
        System.out.println(x);
        x -= 3; //24-3 = 21 
        System.out.println(x);
        x *= 5; //21*5 = 105
        System.out.println(x);
        x /= 2; //105/2 = 52
        System.out.println(x); 
    }
}
