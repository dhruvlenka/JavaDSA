package CoreJava.Operators;

public class OROperator {
    public static void main(String[] args) {
        int x = 18;
        int y = 14;
        /*
        18 in Binary = 0010
        14 in Binary = 1110
        0010 | 1110 = 1110 = 30 will be printed
        */
        System.out.println("X | Y = "+ (x|y));


    }
}
