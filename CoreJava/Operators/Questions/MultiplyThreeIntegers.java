package CoreJava.Operators.Questions;

public class MultiplyThreeIntegers {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 30;
        int d = a*b*c;

        //using a forth variable
        System.out.println(d);

        //directly multiplying using variable
        System.out.println(a*b*c);

        //direclty passing the values
        System.out.println(10*20*30);
    }
}
