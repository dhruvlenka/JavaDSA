package CoreJava.Operators.Questions;

public class TwoFloatNumbers {
    public static void main(String[] args) {
        float f1 = 9.5f;
        float f2 = 10.2f;

        System.out.println(f1 > f2);
        System.out.println(f1 < f2);
        System.out.println(f1 == f2);
        System.out.println(f1 != f2);
    }
}
