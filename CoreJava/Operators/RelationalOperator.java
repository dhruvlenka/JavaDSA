package CoreJava.Operators;

public class RelationalOperator {
    public static void main(String[] args)
    {
      int dhruv = 21;
      int lenka = 19;
    
      System.out.println(dhruv == lenka);
      System.out.println(dhruv != lenka);
      System.out.println(dhruv > lenka);
      System.out.println(dhruv < lenka);
      System.out.println(dhruv >= lenka);
      System.out.println(dhruv <= lenka);

    }
}

