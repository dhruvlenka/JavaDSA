package CoreJava.Operators;

// Right Shift Operator (Division)
public class RightShiftOperator {
    public static void main(String[] args) {
        System.out.println(21>>2); //21/2n = 22/2*2 = 
        System.out.println(18>>3); //18/2n = 18/2*3 =
        System.out.println(19>>4); //19/2n = 19/2*4 = 
        System.out.println(20>>5); //20/2n = 20/2*5 = 
    }
}
